function colorchecker(x){
	if(x <=  0) x=a0
	if(x <= 10) x=a1;    
	if(x <= 20) x=a2;
	if(x <= 30) x=a3;
	if(x <= 40) x=a4;
	if(x <= 50) x=a5;
	if(x <= 60) x=a6;                                          
	if(x <= 70) x=a7;
	if(x <= 80) x=a8;
	if(x <= 90) x=a9;
	if(x >  90) x=a10;
	return x;            
}